#type conversion

value = input("Enter your value: ")
print(value)
print(type(value))

#int - convert value to int
n1 = int(value)
print(n1)
print(type(n1))
print(n1+5)

#float - convert value to float
f1 = float(value)
print(f1)
print(type(f1))
print(f1+5)


#str - convert value to str
i = 2
s = str(i)
print(type(i))
print(type(s))
print("length: ",len(s))

